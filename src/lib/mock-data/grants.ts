export function getGrantById(id: string): Grant | undefined {
  return grants.find((g) => g.id === id);
}

export function getActiveGrants(): Grant[] {
  return grants.filter((g) => g.status === "active");
}

export function getDashboardTotals() {
  const active = getActiveGrants();
  const totalBudget = active.reduce((s, g) => s + g.totalBudget, 0);
  const committed = active.reduce((s, g) => s + g.committed, 0);
  const spent = active.reduce((s, g) => s + g.spent, 0);
  const available = active.reduce((s, g) => s + g.available, 0);
  return { totalBudget, committed, spent, available, activeCount: active.length };
}
